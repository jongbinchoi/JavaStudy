package chap_01;

public class _02_DataTypes {
    //psvm 또는 main입력 했을때 메인이 자동완성이됨.
    public static void main(String[] args) {
        System.out.println("Hello World"); //sout이라고 입력하면 자동 완성이됨.
        System.out.println("안녕하세요?");
        System.out.println(12);
        System.out.println(-34);
        System.out.println(3.14);
        System.out.println(true); //참, 무한대
        System.out.println(false); //거짓

        System.out.println(123 + 345); //자동으로 계산해줌, 가속성을 위해서는 적당한 띄어쓰기 필요
    }
}
